async function updateMaterials() {
    try {
        await mongoose.connect(dbURI);
        console.log("Connected to DB for update...");
        
        for (let mat of translatedMaterials) {
            await Material.findByIdAndUpdate(mat._id, {
                title: mat.title,
                desc: mat.desc,
                content: mat.content
            });
            console.log(`Updated: ${mat.title}`);
        }
        
        console.log("✅ All materials translated and updated!");
        process.exit(0);
    } catch (err) {
        console.error(err);
        process.exit(1);
    }
}

updateMaterials();
