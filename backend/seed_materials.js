async function seed() {
    try {
        await mongoose.connect(dbURI);
        console.log("✅ Connected to MongoDB");
        
        // Додаємо нові матеріали, не видаляючи старі, 
        // або можна оновити існуючі за назвою.
        for (const mat of materials) {
            await Material.findOneAndUpdate(
                { title: mat.title },
                mat,
                { upsert: true, new: true }
            );
        }
        
        console.log("✅ Seeded materials successfully");
        process.exit(0);
    } catch (err) {
        console.error("❌ Error seeding materials:", err);
        process.exit(1);
    }
}

seed();
